create index if not exists nl_sales_reports_started_by_staff_id_idx on public.nl_sales_reports (started_by_staff_id);

create table if not exists public.nl_sales_items (
  id bigint generated by default as identity primary key,
  report_id bigint not null references public.nl_sales_reports(id) on delete cascade,
  line_order integer not null default 1,
  product_name text not null,
  quantity integer not null default 1,
  sale_unit_price_inc_gst numeric(12,2) not null,
  invoice_number text not null default '',
  sale_notes text not null default '',
  entered_by_staff_name text not null,
  cost_unit_price_ex_gst numeric(12,2),
  cost_notes text not null default '',
  cost_updated_at timestamptz,
