$$;

create or replace function public.save_nl_report_costs(session_token text, target_report_id bigint, costs jsonb)
returns jsonb language plpgsql security definer set search_path = public as $$
declare
  report_row public.nl_sales_reports%rowtype;
  cost_json jsonb;
  item_id_value bigint;
  cost_value numeric(12,2);
begin
  if not public.is_valid_admin_session(session_token) then raise exception 'Invalid admin session'; end if;
  if jsonb_typeof(costs) <> 'array' then raise exception 'Costs must be an array'; end if;
  select * into report_row from public.nl_sales_reports where id = target_report_id for update;
  if not found then raise exception 'NL report not found'; end if;
  if report_row.status in ('draft', 'confirmed', 'settled') then
    raise exception 'Costs can only be edited after submission and before confirmation';
  end if;
  for cost_json in select value from jsonb_array_elements(costs) loop
    item_id_value := nullif(cost_json->>'item_id', '')::bigint;
    cost_value := nullif(cost_json->>'cost_unit_price_ex_gst', '')::numeric(12,2);
    if item_id_value is null then raise exception 'Item id is required'; end if;
    if cost_value is not null and cost_value < 0 then raise exception 'Cost price cannot be negative'; end if;
    update public.nl_sales_items set cost_unit_price_ex_gst = cost_value,
      cost_notes = coalesce(trim(cost_json->>'cost_notes'), ''), cost_updated_at = now()
    where id = item_id_value and report_id = target_report_id;
    if not found then raise exception 'NL report item not found'; end if;
  end loop;
  update public.nl_sales_reports set status = case when exists (
    select 1 from public.nl_sales_items item where item.report_id = target_report_id and item.cost_unit_price_ex_gst is null
  ) then 'pricing_pending' else 'priced' end where id = target_report_id;
  return jsonb_build_object('ok', true, 'report', public.get_nl_sales_report_json(target_report_id));
end;
$$;

create or replace function public.update_nl_report_status(session_token text, target_report_id bigint, target_status text)
returns jsonb language plpgsql security definer set search_path = public as $$
declare
  report_row public.nl_sales_reports%rowtype;
  next_status text;
begin
  if not public.is_valid_admin_session(session_token) then raise exception 'Invalid admin session'; end if;
  next_status := lower(coalesce(trim(target_status), ''));
  select * into report_row from public.nl_sales_reports where id = target_report_id for update;
  if not found then raise exception 'NL report not found'; end if;
  if next_status = 'confirmed' then
    if report_row.status <> 'priced' then raise exception 'Every product needs a cost before confirmation'; end if;
    update public.nl_sales_reports set status = 'confirmed', confirmed_at = now() where id = target_report_id;
  elsif next_status = 'settled' then
    if report_row.status <> 'confirmed' then raise exception 'Confirm the report before marking it settled'; end if;
    update public.nl_sales_reports set status = 'settled', settled_at = now() where id = target_report_id;
  else
    raise exception 'Unsupported NL report status';
  end if;
  return jsonb_build_object('ok', true, 'report', public.get_nl_sales_report_json(target_report_id));
end;
$$;

create or replace function public.change_nl_report_password(session_token text, new_password text)
returns jsonb language plpgsql security definer set search_path = public as $$
begin
  if not public.is_valid_admin_session(session_token) then raise exception 'Invalid admin session'; end if;
  if length(coalesce(trim(new_password), '')) < 4 then raise exception 'NL password must be at least 4 characters'; end if;
  update public.nl_report_config set password_hash = extensions.crypt(trim(new_password), extensions.gen_salt('bf', 12)) where id = 1;
  delete from public.nl_report_sessions;
  return jsonb_build_object('ok', true);
end;
$$;

