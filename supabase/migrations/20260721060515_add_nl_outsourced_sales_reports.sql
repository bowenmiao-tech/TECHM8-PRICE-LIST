create or replace function public.is_valid_nl_report_session(session_token text)
returns boolean language plpgsql security definer set search_path = public as $$
begin
  if coalesce(session_token, '') = '' then return false; end if;
  delete from public.nl_report_sessions where expires_at <= now();
  return exists (
    select 1 from public.nl_report_sessions
    where session_hash = encode(extensions.digest(session_token, 'sha256'), 'hex') and expires_at > now()
  );
end;
$$;

create or replace function public.create_nl_report_session(staff_session_token text, input_password text, target_staff_name text)
returns jsonb language plpgsql security definer set search_path = public as $$
declare
  selected_staff public.staff_directory%rowtype;
  selected_store public.store_locations%rowtype;
  stored_hash text;
  raw_token text;
begin
  if not public.is_valid_staff_session(staff_session_token) then raise exception 'Invalid staff session'; end if;
  select password_hash into stored_hash from public.nl_report_config where id = 1;
  if stored_hash is null or extensions.crypt(coalesce(input_password, ''), stored_hash) <> stored_hash then
    raise exception 'Incorrect NL report password';
  end if;
  select * into selected_staff from public.staff_directory
  where active = true and display_name = coalesce(trim(target_staff_name), '');
  if not found then raise exception 'Staff member not found'; end if;
  select * into selected_store from public.store_locations where active = true and store_code = 'northlakes';
  if not found then raise exception 'North Lakes store not found'; end if;
  delete from public.nl_report_sessions where expires_at <= now();
  raw_token := encode(extensions.gen_random_bytes(32), 'hex');
  insert into public.nl_report_sessions (session_hash, store_id, staff_id, staff_name, expires_at)
  values (encode(extensions.digest(raw_token, 'sha256'), 'hex'), selected_store.id, selected_staff.id,
    selected_staff.display_name, now() + interval '12 hours');
  return jsonb_build_object('ok', true, 'session_token', raw_token, 'staff_name', selected_staff.display_name,
    'store_code', selected_store.store_code, 'store_name', selected_store.store_name,
    'expires_at', now() + interval '12 hours');
end;
$$;

create or replace function public.verify_nl_report_session(session_token text)
returns jsonb language sql security definer set search_path = public as $$
  select jsonb_build_object('ok', public.is_valid_nl_report_session(session_token));
$$;

create or replace function public.revoke_nl_report_session(session_token text)
returns jsonb language plpgsql security definer set search_path = public as $$
begin
  delete from public.nl_report_sessions
  where session_hash = encode(extensions.digest(coalesce(session_token, ''), 'sha256'), 'hex');
  return jsonb_build_object('ok', true);
end;
$$;

create or replace function public.get_nl_sales_report_json(target_report_id bigint)
returns jsonb language sql security definer set search_path = public as $$
  select jsonb_build_object(
    'id', report.id, 'report_date', report.report_date, 'store_name', report.store_name,
    'status', report.status, 'started_by_staff_name', report.started_by_staff_name,
    'submitted_by_staff_name', report.submitted_by_staff_name, 'submitted_at', report.submitted_at,
    'confirmed_at', report.confirmed_at, 'settled_at', report.settled_at, 'updated_at', report.updated_at,
    'sales_total_inc_gst', coalesce((select round(sum(item.sale_unit_price_inc_gst * item.quantity), 2)
      from public.nl_sales_items item where item.report_id = report.id), 0),
    'sales_gst', coalesce((select round(sum(item.sale_unit_price_inc_gst * item.quantity) / 11, 2)
      from public.nl_sales_items item where item.report_id = report.id), 0),
    'payable_total_inc_gst', coalesce((select round(sum(round(item.cost_unit_price_ex_gst * 1.10, 2) * item.quantity), 2)
      from public.nl_sales_items item where item.report_id = report.id and item.cost_unit_price_ex_gst is not null), 0),
    'payable_cost_ex_gst', coalesce((select round(sum(item.cost_unit_price_ex_gst * item.quantity), 2)
      from public.nl_sales_items item where item.report_id = report.id and item.cost_unit_price_ex_gst is not null), 0),
    'payable_gst', coalesce((select round(sum(round(item.cost_unit_price_ex_gst * 1.10, 2) * item.quantity) / 11, 2)
      from public.nl_sales_items item where item.report_id = report.id and item.cost_unit_price_ex_gst is not null), 0),
    'pending_cost_count', (select count(*) from public.nl_sales_items item
      where item.report_id = report.id and item.cost_unit_price_ex_gst is null),
    'items', coalesce((select jsonb_agg(jsonb_build_object(
      'id', item.id, 'line_order', item.line_order, 'product_name', item.product_name,
      'quantity', item.quantity, 'sale_unit_price_inc_gst', item.sale_unit_price_inc_gst,
      'sale_total_inc_gst', round(item.sale_unit_price_inc_gst * item.quantity, 2),
      'invoice_number', item.invoice_number, 'sale_notes', item.sale_notes,
      'entered_by_staff_name', item.entered_by_staff_name,
      'cost_unit_price_ex_gst', item.cost_unit_price_ex_gst,
      'cost_unit_price_inc_gst', case when item.cost_unit_price_ex_gst is null then null else round(item.cost_unit_price_ex_gst * 1.10, 2) end,
      'cost_total_inc_gst', case when item.cost_unit_price_ex_gst is null then null else round(round(item.cost_unit_price_ex_gst * 1.10, 2) * item.quantity, 2) end,
      'cost_notes', item.cost_notes, 'cost_updated_at', item.cost_updated_at
    ) order by item.line_order, item.id) from public.nl_sales_items item where item.report_id = report.id), '[]'::jsonb)
  ) from public.nl_sales_reports report where report.id = target_report_id;
$$;

create or replace function public.get_nl_sales_report_partner_json(target_report_id bigint)
returns jsonb language sql security definer set search_path = public as $$
  with base as (
    select public.get_nl_sales_report_json(target_report_id) as payload
  ), redacted_items as (
    select coalesce(jsonb_agg(
      (item.value
        - 'cost_unit_price_ex_gst'
        - 'cost_unit_price_inc_gst'
        - 'cost_total_inc_gst'
        - 'cost_notes'
        - 'cost_updated_at')
      || jsonb_build_object(
        'cost_unit_price_ex_gst', null,
        'cost_unit_price_inc_gst', null,
        'cost_total_inc_gst', null,
        'cost_notes', '',
        'cost_updated_at', null
      )
      order by (item.value->>'line_order')::integer
    ), '[]'::jsonb) as payload
    from base
    cross join lateral jsonb_array_elements(coalesce(base.payload->'items', '[]'::jsonb)) item
  )
  select case
    when base.payload->>'status' in ('confirmed', 'settled') then base.payload
    else base.payload || jsonb_build_object(
      'payable_total_inc_gst', 0,
      'payable_cost_ex_gst', 0,
      'payable_gst', 0,
      'pending_cost_count', jsonb_array_length(coalesce(base.payload->'items', '[]'::jsonb)),
      'items', redacted_items.payload
    )
  end
  from base cross join redacted_items;
$$;

create or replace function public.get_nl_sales_report(session_token text, target_date text default null)
returns jsonb language plpgsql security definer set search_path = public as $$
declare
  session_row public.nl_report_sessions%rowtype;
  selected_report_id bigint;
  target_date_value date;
begin
  if not public.is_valid_nl_report_session(session_token) then raise exception 'Invalid NL report session'; end if;
  select * into session_row from public.nl_report_sessions
  where session_hash = encode(extensions.digest(session_token, 'sha256'), 'hex') and expires_at > now();
  target_date_value := coalesce(nullif(target_date, '')::date, (now() at time zone 'Australia/Brisbane')::date);
  select id into selected_report_id from public.nl_sales_reports
  where store_id = session_row.store_id and report_date = target_date_value;
  return jsonb_build_object('ok', true, 'staff_name', session_row.staff_name, 'report_date', target_date_value,
    'report', case when selected_report_id is null then null else public.get_nl_sales_report_partner_json(selected_report_id) end);
end;
$$;

create or replace function public.save_nl_sales_report(session_token text, payload jsonb)
returns jsonb language plpgsql security definer set search_path = public as $$
declare
  session_row public.nl_report_sessions%rowtype;
  selected_store public.store_locations%rowtype;
  report_row public.nl_sales_reports%rowtype;
  target_date_value date;
  items_json jsonb;
  item_json jsonb;
  item_index integer := 0;
  item_quantity integer;
  item_sale_price numeric(12,2);
begin
  if not public.is_valid_nl_report_session(session_token) then raise exception 'Invalid NL report session'; end if;
  if jsonb_typeof(payload) <> 'object' then raise exception 'Payload must be an object'; end if;
  select * into session_row from public.nl_report_sessions
  where session_hash = encode(extensions.digest(session_token, 'sha256'), 'hex') and expires_at > now();
  select * into selected_store from public.store_locations where id = session_row.store_id;
  target_date_value := coalesce(nullif(payload->>'report_date', '')::date, (now() at time zone 'Australia/Brisbane')::date);
  items_json := coalesce(payload->'items', '[]'::jsonb);
  if jsonb_typeof(items_json) <> 'array' then raise exception 'Items must be an array'; end if;
  if jsonb_array_length(items_json) > 500 then raise exception 'A report cannot contain more than 500 items'; end if;
  select * into report_row from public.nl_sales_reports
  where store_id = session_row.store_id and report_date = target_date_value for update;
  if not found then
    insert into public.nl_sales_reports (report_date, store_id, store_name, started_by_staff_id, started_by_staff_name)
    values (target_date_value, session_row.store_id, selected_store.store_name, session_row.staff_id, session_row.staff_name)
    returning * into report_row;
  elsif report_row.status <> 'draft' then
    raise exception 'This report has already been submitted and is read-only';
  end if;
  delete from public.nl_sales_items where report_id = report_row.id;
  for item_json in select value from jsonb_array_elements(items_json) loop
    item_index := item_index + 1;
    if coalesce(trim(item_json->>'product_name'), '') = '' then raise exception 'Product name is required on line %', item_index; end if;
    begin
      item_quantity := coalesce(nullif(item_json->>'quantity', '')::integer, 1);
      item_sale_price := nullif(item_json->>'sale_unit_price_inc_gst', '')::numeric(12,2);
    exception when others then raise exception 'Invalid quantity or sale price on line %', item_index;
    end;
    if item_quantity <= 0 then raise exception 'Quantity must be greater than zero on line %', item_index; end if;
    if item_sale_price is null or item_sale_price < 0 then raise exception 'Sale price is required on line %', item_index; end if;
    insert into public.nl_sales_items (report_id, line_order, product_name, quantity, sale_unit_price_inc_gst,
      invoice_number, sale_notes, entered_by_staff_name)
    values (report_row.id, item_index, trim(item_json->>'product_name'), item_quantity, item_sale_price,
      coalesce(trim(item_json->>'invoice_number'), ''), coalesce(trim(item_json->>'sale_notes'), ''), session_row.staff_name);
  end loop;
  update public.nl_sales_reports set updated_at = now() where id = report_row.id;
  return jsonb_build_object('ok', true, 'report', public.get_nl_sales_report_json(report_row.id));
end;
$$;

create or replace function public.submit_nl_sales_report(session_token text, target_date text default null)
returns jsonb language plpgsql security definer set search_path = public as $$
declare
  session_row public.nl_report_sessions%rowtype;
  report_row public.nl_sales_reports%rowtype;
  target_date_value date;
begin
  if not public.is_valid_nl_report_session(session_token) then raise exception 'Invalid NL report session'; end if;
  select * into session_row from public.nl_report_sessions
  where session_hash = encode(extensions.digest(session_token, 'sha256'), 'hex') and expires_at > now();
  target_date_value := coalesce(nullif(target_date, '')::date, (now() at time zone 'Australia/Brisbane')::date);
  select * into report_row from public.nl_sales_reports
  where store_id = session_row.store_id and report_date = target_date_value for update;
  if not found then raise exception 'Save the report before submitting it'; end if;
  if report_row.status <> 'draft' then raise exception 'This report has already been submitted'; end if;
  update public.nl_sales_reports set
    status = case when exists (select 1 from public.nl_sales_items item
      where item.report_id = report_row.id and item.cost_unit_price_ex_gst is null) then 'pricing_pending' else 'priced' end,
    submitted_by_staff_name = session_row.staff_name, submitted_at = now()
  where id = report_row.id;
  return jsonb_build_object('ok', true, 'report', public.get_nl_sales_report_json(report_row.id));
end;
$$;

create or replace function public.list_nl_sales_reports(session_token text, date_from text default null, date_to text default null)
returns jsonb language plpgsql security definer set search_path = public as $$
declare
  session_row public.nl_report_sessions%rowtype;
  from_date_value date;
  to_date_value date;
  reports_json jsonb;
begin
  if not public.is_valid_nl_report_session(session_token) then raise exception 'Invalid NL report session'; end if;
  select * into session_row from public.nl_report_sessions
  where session_hash = encode(extensions.digest(session_token, 'sha256'), 'hex') and expires_at > now();
  to_date_value := coalesce(nullif(date_to, '')::date, (now() at time zone 'Australia/Brisbane')::date);
  from_date_value := coalesce(nullif(date_from, '')::date, date_trunc('month', to_date_value)::date);
  if from_date_value > to_date_value then raise exception 'From date cannot be after to date'; end if;
  if to_date_value - from_date_value > 366 then raise exception 'Date range cannot exceed 366 days'; end if;
  select coalesce(jsonb_agg(public.get_nl_sales_report_partner_json(report.id) order by report.report_date desc, report.id desc), '[]'::jsonb)
  into reports_json from public.nl_sales_reports report
  where report.store_id = session_row.store_id and report.report_date between from_date_value and to_date_value;
  return jsonb_build_object('ok', true, 'reports', reports_json);
end;
$$;

create or replace function public.get_nl_sales_reports_admin(session_token text, date_from text default null, date_to text default null)
returns jsonb language plpgsql security definer set search_path = public as $$
declare
  from_date_value date;
  to_date_value date;
  reports_json jsonb;
begin
  if not public.is_valid_admin_session(session_token) then raise exception 'Invalid admin session'; end if;
  to_date_value := coalesce(nullif(date_to, '')::date, (now() at time zone 'Australia/Brisbane')::date);
  from_date_value := coalesce(nullif(date_from, '')::date, date_trunc('month', to_date_value)::date);
  if from_date_value > to_date_value then raise exception 'From date cannot be after to date'; end if;
