  constraint nl_sales_items_sale_price_check check (sale_unit_price_inc_gst >= 0),
  constraint nl_sales_items_cost_price_check check (cost_unit_price_ex_gst is null or cost_unit_price_ex_gst >= 0)
);

create index if not exists nl_sales_items_report_idx on public.nl_sales_items (report_id, line_order, id);

