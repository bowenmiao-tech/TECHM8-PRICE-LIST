create or replace function public.search_pos_repair_tickets(
  session_token text,
  target_store_code text,
  search_query text default '',
  result_limit integer default 200
)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  selected_store public.store_locations%rowtype;
  tickets_payload jsonb;
  query_value text := trim(coalesce(search_query, ''));
  phone_query text := regexp_replace(coalesce(search_query, ''), '[^0-9]', '', 'g');
  safe_limit integer := least(greatest(coalesce(result_limit, 200), 1), 500);
begin
  if not public.is_valid_staff_session(session_token) then
    raise exception 'Invalid session';
  end if;

  select * into selected_store
  from public.store_locations store_location
  where store_location.active = true
    and store_location.store_code = coalesce(trim(target_store_code), '')
    and store_location.store_code <> 'warehouse';

  if not found then
    raise exception 'Store not found';
  end if;

  select coalesce(
    jsonb_agg(public.pos_repair_ticket_payload(ticket) order by ticket.status_updated_at desc, ticket.created_at desc),
    '[]'::jsonb
  )
  into tickets_payload
  from (
    select repair_ticket.*
    from public.pos_repair_tickets repair_ticket
    where repair_ticket.store_id = selected_store.id
      and repair_ticket.active = true
      and repair_ticket.closed_at is null
      and (
        query_value = ''
        or repair_ticket.ticket_code ilike '%' || query_value || '%'
        or repair_ticket.customer_name ilike '%' || query_value || '%'
        or repair_ticket.customer_phone ilike '%' || query_value || '%'
        or (phone_query <> '' and regexp_replace(repair_ticket.customer_phone, '[^0-9]', '', 'g') like '%' || phone_query || '%')
        or repair_ticket.title ilike '%' || query_value || '%'
        or repair_ticket.issue ilike '%' || query_value || '%'
        or repair_ticket.intake::text ilike '%' || query_value || '%'
      )
    order by repair_ticket.status_updated_at desc, repair_ticket.created_at desc
    limit safe_limit
  ) ticket;

  return jsonb_build_object(
    'ok', true,
    'store_code', selected_store.store_code,
    'store_name', selected_store.store_name,
    'tickets', tickets_payload
  );
end;
$$;

create or replace function public.get_pos_repair_tickets(session_token text, target_store_code text)
returns jsonb
language sql
security definer
set search_path = ''
as $$
  select public.search_pos_repair_tickets(session_token, target_store_code, '', 500);
$$;

create or replace function public.upsert_pos_repair_ticket(session_token text, payload jsonb)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  selected_store public.store_locations%rowtype;
  selected_staff public.staff_directory%rowtype;
  ticket_code_value text;
  status_value text;
  customer_name_value text;
  customer_phone_value text;
  resolution_value text;
  created_at_value timestamptz;
  status_updated_at_value timestamptz;
  ticket_row public.pos_repair_tickets%rowtype;
begin
  if not public.is_valid_staff_session(session_token) then
    raise exception 'Invalid session';
  end if;
  if jsonb_typeof(payload) <> 'object' then
    raise exception 'Payload must be a JSON object';
  end if;

  ticket_code_value := coalesce(nullif(trim(payload->>'ticket_code'), ''), nullif(trim(payload->>'id'), ''));
  if ticket_code_value is null then raise exception 'Ticket id is required'; end if;

  select * into selected_store
  from public.store_locations store_location
  where store_location.active = true
    and store_location.store_code = coalesce(
      nullif(trim(payload->>'store_code'), ''),
      nullif(trim(payload->>'storeId'), ''),
      nullif(trim(payload->>'store_id'), '')
    )
    and store_location.store_code <> 'warehouse';
  if not found then raise exception 'Store not found'; end if;

  select * into selected_staff
  from public.staff_directory staff
  where staff.active = true
    and lower(staff.display_name) = lower(coalesce(trim(payload->>'staff_name'), trim(payload->>'updatedBy'), trim(payload->>'createdBy'), ''))
  limit 1;
  if not found then raise exception 'Staff member not found'; end if;

  customer_name_value := coalesce(nullif(trim(payload->>'customerName'), ''), '');
  customer_phone_value := coalesce(
    nullif(trim(payload->>'customerPhone'), ''),
    nullif(trim(payload->>'customerContact'), ''),
    ''
  );
  if customer_name_value = '' or lower(customer_name_value) = 'walk-in customer' then
    raise exception 'Customer name is required for repair tickets';
  end if;
  if customer_phone_value = '' then
    raise exception 'Customer phone is required for repair tickets';
  end if;

  status_value := coalesce(nullif(trim(payload->>'status'), ''), 'repairing');
  if status_value not in ('need_to_order', 'waiting_shipping', 'repairing', 'waiting_pickup', 'waiting_customer_confirmation', 'over_3_months_uncollected') then
    raise exception 'Invalid ticket status';
  end if;

  resolution_value := nullif(trim(payload->>'resolution'), '');
  if resolution_value is not null and resolution_value not in ('repaired', 'cancelled', 'returned_unrepaired') then
    raise exception 'Invalid repair resolution';
  end if;

  created_at_value := coalesce(nullif(payload->>'createdAt', '')::timestamptz, now());
  status_updated_at_value := coalesce(nullif(payload->>'statusUpdatedAt', '')::timestamptz, now());

  insert into public.pos_repair_tickets (
    ticket_code, store_id, title, issue, price, status, device_in_store,
    motherboard_repair, special_order, customer_name, customer_contact,
    customer_phone, resolution, ready_for_pickup_at, created_by, updated_by,
    status_updated_at, intake, activity, comments, active, created_at
  ) values (
    ticket_code_value,
    selected_store.id,
    coalesce(trim(payload->>'title'), ''),
    coalesce(trim(payload->>'issue'), ''),
    coalesce(trim(payload->>'price'), ''),
    status_value,
    lower(coalesce(payload->>'deviceInStore', 'false')) = 'true',
    lower(coalesce(payload->>'motherboardRepair', 'false')) = 'true',
    lower(coalesce(payload->>'specialOrder', 'false')) = 'true',
    customer_name_value,
    customer_phone_value,
    customer_phone_value,
    resolution_value,
    case when status_value in ('waiting_pickup', 'over_3_months_uncollected') then status_updated_at_value else null end,
    coalesce(nullif(trim(payload->>'createdBy'), ''), selected_staff.display_name),
    selected_staff.display_name,
    status_updated_at_value,
    coalesce(payload->'intake', '{}'::jsonb),
    coalesce(payload->'activity', '[]'::jsonb),
    coalesce(payload->'comments', '[]'::jsonb),
    true,
    created_at_value
  )
  on conflict (ticket_code) do update set
    store_id = excluded.store_id,
    title = excluded.title,
    issue = excluded.issue,
    price = excluded.price,
    status = excluded.status,
    device_in_store = excluded.device_in_store,
    motherboard_repair = excluded.motherboard_repair,
    special_order = excluded.special_order,
    customer_name = excluded.customer_name,
    customer_contact = excluded.customer_phone,
    customer_phone = excluded.customer_phone,
    resolution = excluded.resolution,
    ready_for_pickup_at = case
      when excluded.status in ('waiting_pickup', 'over_3_months_uncollected')
        then coalesce(public.pos_repair_tickets.ready_for_pickup_at, excluded.status_updated_at)
      else null
    end,
    updated_by = excluded.updated_by,
    status_updated_at = excluded.status_updated_at,
    intake = excluded.intake,
    activity = excluded.activity,
    comments = excluded.comments,
    active = true
  returning * into ticket_row;

  return jsonb_build_object('ok', true, 'ticket', public.pos_repair_ticket_payload(ticket_row));
end;
$$;

create or replace function public.save_pos_sales_order(session_token text, payload jsonb)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  selected_store public.store_locations%rowtype;
  selected_staff public.staff_directory%rowtype;
  saved_order public.pos_sales_orders%rowtype;
  order_code_value text;
  created_at_value timestamptz;
  business_date_value date;
  total_value numeric(12,2);
  item_total numeric(12,2);
  payment_total numeric(12,2);
  next_invoice_number bigint;
  repair_item_count integer;
  repair_ticket_count integer;
begin
  if not public.is_valid_staff_session(session_token) then raise exception 'Invalid session'; end if;
  if jsonb_typeof(payload) <> 'object' then raise exception 'Order payload must be a JSON object'; end if;

  order_code_value := coalesce(trim(payload->>'id'), '');
  if order_code_value = '' then raise exception 'Order id is required'; end if;
  if jsonb_typeof(payload->'items') <> 'array' or jsonb_array_length(payload->'items') = 0 then
    raise exception 'Order must contain at least one item';
  end if;
  if jsonb_typeof(payload->'payments') <> 'array' or jsonb_array_length(payload->'payments') = 0 then
    raise exception 'Order must contain at least one payment';
  end if;

  total_value := round(coalesce(nullif(payload->>'total', '')::numeric, 0), 2);
  if total_value < 0 then raise exception 'Order total cannot be negative'; end if;

  select round(coalesce(sum(coalesce(nullif(payment->>'amount', '')::numeric, 0)), 0), 2)
  into payment_total from jsonb_array_elements(payload->'payments') payment;
  if payment_total < total_value then raise exception 'Payment total is less than order total'; end if;

  select round(coalesce(sum(
    coalesce(
      nullif(item->>'line_total', '')::numeric,
      coalesce(nullif(item->>'unit_price', '')::numeric, nullif(item->>'sale_price', '')::numeric, 0)
        * greatest(coalesce(nullif(item->>'qty', '')::integer, 1), 1)
    )
  ), 0), 2)
  into item_total from jsonb_array_elements(payload->'items') item;
  if item_total <> total_value then raise exception 'Order item total does not match order total'; end if;

  select * into selected_store
  from public.store_locations store_location
  where store_location.active = true
    and (
      lower(regexp_replace(store_location.store_code, '[^a-z0-9]', '', 'g')) =
        lower(regexp_replace(coalesce(payload->>'store_db_code', payload->>'store_id', ''), '[^a-z0-9]', '', 'g'))
      or upper(store_location.store_code) = upper(coalesce(payload->>'store_code', ''))
    )
  limit 1;
  if not found then raise exception 'Store not found'; end if;

  select * into selected_staff
  from public.staff_directory staff
  where staff.active = true and lower(staff.display_name) = lower(coalesce(trim(payload->>'staff_name'), ''))
  limit 1;
  if not found then raise exception 'Staff member not found'; end if;

  created_at_value := coalesce(nullif(payload->>'created_at', '')::timestamptz, now());
  business_date_value := coalesce(nullif(payload->>'business_date', '')::date, (created_at_value at time zone 'Australia/Brisbane')::date);

  perform pg_catalog.pg_advisory_xact_lock(pg_catalog.hashtextextended(order_code_value, 0));
  select * into saved_order
  from public.pos_sales_orders sales_order
  where sales_order.order_code = order_code_value
  for update;

  if found then
    if saved_order.store_id <> selected_store.id then raise exception 'Order store cannot be changed'; end if;
    return jsonb_build_object('ok', true, 'order', public.pos_sales_order_payload(saved_order));
  end if;

  select count(*) into repair_item_count
  from jsonb_array_elements(payload->'items') item
  where lower(coalesce(item->>'is_repair', 'false')) = 'true';

  if repair_item_count > 0 then
    if coalesce(trim(payload->>'customer_name'), '') = '' or lower(trim(payload->>'customer_name')) = 'walk-in customer' then
      raise exception 'Customer name is required for repair sales';
    end if;
    if coalesce(trim(payload->>'customer_phone'), '') = '' then
      raise exception 'Customer phone is required for repair sales';
    end if;

    if exists (
      select 1 from jsonb_array_elements(payload->'items') item
      where lower(coalesce(item->>'is_repair', 'false')) = 'true'
        and coalesce(
          nullif(trim(item->>'ticket_id'), ''),
          nullif(regexp_replace(coalesce(item->>'product_id', ''), '^repair-', ''), coalesce(item->>'product_id', ''))
        ) is null
    ) then
      raise exception 'Repair sale item is missing ticket id';
    end if;

    perform 1
    from public.pos_repair_tickets repair_ticket
    where repair_ticket.ticket_code in (
      select coalesce(
        nullif(trim(item->>'ticket_id'), ''),
        nullif(regexp_replace(coalesce(item->>'product_id', ''), '^repair-', ''), coalesce(item->>'product_id', ''))
      )
      from jsonb_array_elements(payload->'items') item
      where lower(coalesce(item->>'is_repair', 'false')) = 'true'
    )
    for update;

    select count(*) into repair_ticket_count
    from public.pos_repair_tickets repair_ticket
    where repair_ticket.ticket_code in (
      select coalesce(
        nullif(trim(item->>'ticket_id'), ''),
        nullif(regexp_replace(coalesce(item->>'product_id', ''), '^repair-', ''), coalesce(item->>'product_id', ''))
      )
      from jsonb_array_elements(payload->'items') item
      where lower(coalesce(item->>'is_repair', 'false')) = 'true'
    )
      and repair_ticket.store_id = selected_store.id
      and repair_ticket.active = true
      and repair_ticket.closed_at is null;

    if repair_ticket_count <> repair_item_count then
      raise exception 'Repair ticket is missing, closed, or belongs to another store';
    end if;

    if exists (
      select 1
      from public.pos_sales_order_lines sales_line
      join public.pos_repair_tickets repair_ticket on repair_ticket.id = sales_line.repair_ticket_id
      where repair_ticket.ticket_code in (
        select coalesce(
          nullif(trim(item->>'ticket_id'), ''),
          nullif(regexp_replace(coalesce(item->>'product_id', ''), '^repair-', ''), coalesce(item->>'product_id', ''))
        )
        from jsonb_array_elements(payload->'items') item
        where lower(coalesce(item->>'is_repair', 'false')) = 'true'
      )
    ) then
      raise exception 'Repair ticket has already been invoiced';
    end if;
  end if;

  insert into public.pos_store_invoice_counters (store_id, last_number)
  values (selected_store.id, 1)
  on conflict (store_id) do update set
    last_number = public.pos_store_invoice_counters.last_number + 1,
    updated_at = now()
  returning last_number into next_invoice_number;

  insert into public.pos_sales_orders (
    order_code, invoice_number, store_id, business_date, staff_name, shift_id,
    customer_name, customer_phone, customer_email, payment_method, total,
    order_payload, created_at
  ) values (
    order_code_value,
    next_invoice_number,
    selected_store.id,
    business_date_value,
    selected_staff.display_name,
    nullif(trim(payload->>'shift_id'), ''),
    coalesce(nullif(trim(payload->>'customer_name'), ''), 'Walk-in Customer'),
    coalesce(trim(payload->>'customer_phone'), ''),
    coalesce(trim(payload->>'customer_email'), ''),
    coalesce(nullif(trim(payload->>'payment_method'), ''), 'Unknown'),
    total_value,
    payload || jsonb_build_object('sync_pending', false),
    created_at_value
  ) returning * into saved_order;

  insert into public.pos_sales_order_lines (
    sales_order_id, line_number, line_type, product_id, repair_ticket_id,
    sku, name, category, quantity, unit_price, line_total, line_payload, created_at
  )
  select
    saved_order.id,
    item.ordinality::integer,
    case
      when lower(coalesce(item.value->>'is_repair', 'false')) = 'true' then 'repair'
      when lower(coalesce(item.value->>'is_special', 'false')) = 'true'
        or coalesce(item.value->>'product_id', '') like 'special-%' then 'special'
      else 'product'
    end,
    coalesce(item.value->>'product_id', item.value->>'id', ''),
    repair_ticket.id,
    coalesce(item.value->>'sku', ''),
    coalesce(nullif(trim(item.value->>'name'), ''), 'Sale item'),
    coalesce(item.value->>'category', ''),
    greatest(coalesce(nullif(item.value->>'qty', '')::integer, 1), 1),
    round(coalesce(nullif(item.value->>'unit_price', '')::numeric, nullif(item.value->>'sale_price', '')::numeric, 0), 2),
    round(coalesce(
      nullif(item.value->>'line_total', '')::numeric,
      coalesce(nullif(item.value->>'unit_price', '')::numeric, nullif(item.value->>'sale_price', '')::numeric, 0)
        * greatest(coalesce(nullif(item.value->>'qty', '')::integer, 1), 1)
    ), 2),
    item.value,
    created_at_value
  from jsonb_array_elements(payload->'items') with ordinality as item(value, ordinality)
  left join public.pos_repair_tickets repair_ticket
    on repair_ticket.ticket_code = coalesce(
      nullif(trim(item.value->>'ticket_id'), ''),
      nullif(regexp_replace(coalesce(item.value->>'product_id', ''), '^repair-', ''), coalesce(item.value->>'product_id', ''))
    );

  insert into public.pos_sales_order_payments (sales_order_id, payment_number, method, amount, created_at)
  select
    saved_order.id,
    payment.ordinality::integer,
    coalesce(nullif(trim(payment.value->>'method'), ''), saved_order.payment_method),
    round(coalesce(nullif(payment.value->>'amount', '')::numeric, 0), 2),
    created_at_value
  from jsonb_array_elements(payload->'payments') with ordinality as payment(value, ordinality)
  where coalesce(nullif(payment.value->>'amount', '')::numeric, 0) > 0;

  update public.pos_repair_tickets repair_ticket
  set
    customer_name = saved_order.customer_name,
    customer_phone = saved_order.customer_phone,
    customer_contact = saved_order.customer_phone,
    status = 'waiting_pickup',
    resolution = coalesce(repair_ticket.resolution, 'repaired'),
    ready_for_pickup_at = coalesce(repair_ticket.ready_for_pickup_at, now()),
    closed_at = now(),
    updated_by = selected_staff.display_name,
    status_updated_at = now(),
    activity = jsonb_build_array(jsonb_build_object(
      'id', 'ACT-' || floor(extract(epoch from clock_timestamp()) * 1000)::bigint,
      'type', 'paid',
      'text', 'checked out this repair on invoice #' || next_invoice_number,
      'staffName', selected_staff.display_name,
      'at', now()
    )) || coalesce(repair_ticket.activity, '[]'::jsonb)
  where repair_ticket.id in (
    select sales_line.repair_ticket_id
    from public.pos_sales_order_lines sales_line
    where sales_line.sales_order_id = saved_order.id
      and sales_line.repair_ticket_id is not null
  );

  return jsonb_build_object('ok', true, 'order', public.pos_sales_order_payload(saved_order));
end;
$$;

create or replace function public.search_pos_sales_orders(
  session_token text,
  target_store_code text,
  search_query text default '',
  result_limit integer default 100,
  result_offset integer default 0
)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  selected_store public.store_locations%rowtype;
  orders_payload jsonb;
  query_value text := trim(coalesce(search_query, ''));
  phone_query text := regexp_replace(coalesce(search_query, ''), '[^0-9]', '', 'g');
  safe_limit integer := least(greatest(coalesce(result_limit, 100), 1), 200);
  safe_offset integer := greatest(coalesce(result_offset, 0), 0);
begin
  if not public.is_valid_staff_session(session_token) then raise exception 'Invalid session'; end if;

  select * into selected_store
  from public.store_locations store_location
  where store_location.active = true
    and store_location.store_code = coalesce(trim(target_store_code), '')
    and store_location.store_code <> 'warehouse';
  if not found then raise exception 'Store not found'; end if;

  select coalesce(
    jsonb_agg(public.pos_sales_order_payload(sales_order) order by sales_order.created_at desc),
    '[]'::jsonb
  ) into orders_payload
  from (
    select sales_order.*
    from public.pos_sales_orders sales_order
    where sales_order.store_id = selected_store.id
      and (
        query_value = ''
        or sales_order.invoice_number::text ilike '%' || query_value || '%'
        or sales_order.order_code ilike '%' || query_value || '%'
        or sales_order.customer_name ilike '%' || query_value || '%'
        or sales_order.customer_phone ilike '%' || query_value || '%'
        or (phone_query <> '' and regexp_replace(sales_order.customer_phone, '[^0-9]', '', 'g') like '%' || phone_query || '%')
        or exists (
          select 1
          from public.pos_sales_order_lines sales_line
          left join public.pos_repair_tickets repair_ticket on repair_ticket.id = sales_line.repair_ticket_id
          where sales_line.sales_order_id = sales_order.id
            and (
              sales_line.name ilike '%' || query_value || '%'
              or sales_line.sku ilike '%' || query_value || '%'
              or repair_ticket.ticket_code ilike '%' || query_value || '%'
            )
        )
      )
    order by sales_order.created_at desc
    limit safe_limit offset safe_offset
  ) sales_order;

  return jsonb_build_object('ok', true, 'orders', orders_payload);
end;
$$;

create or replace function public.refund_pos_sales_order(session_token text, payload jsonb)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  selected_staff public.staff_directory%rowtype;
  selected_order public.pos_sales_orders%rowtype;
  refund_row public.pos_sales_refunds%rowtype;
  order_code_value text := coalesce(trim(payload->>'order_id'), '');
  reason_value text := coalesce(trim(payload->>'reason'), '');
  method_value text := coalesce(trim(payload->>'refund_method'), '');
  refund_total numeric(12,2);
  requested_count integer;
  valid_count integer;
begin
  if not public.is_valid_staff_session(session_token) then raise exception 'Invalid session'; end if;
  if jsonb_typeof(payload) <> 'object' then raise exception 'Refund payload must be an object'; end if;
  if order_code_value = '' then raise exception 'Order id is required'; end if;
  if reason_value = '' then raise exception 'Refund reason is required'; end if;
  if method_value = '' then raise exception 'Refund method is required'; end if;
  if jsonb_typeof(payload->'lines') <> 'array' or jsonb_array_length(payload->'lines') = 0 then
    raise exception 'Select at least one refund line';
  end if;

  select * into selected_staff
  from public.staff_directory staff
  where staff.active = true and lower(staff.display_name) = lower(coalesce(trim(payload->>'staff_name'), ''))
  limit 1;
  if not found then raise exception 'Staff member not found'; end if;

  perform pg_catalog.pg_advisory_xact_lock(pg_catalog.hashtextextended('refund:' || order_code_value, 0));
  select * into selected_order
  from public.pos_sales_orders sales_order
  where sales_order.order_code = order_code_value
  for update;
  if not found then raise exception 'Invoice not found'; end if;

  select count(*), round(coalesce(sum(coalesce(nullif(line->>'amount', '')::numeric, 0)), 0), 2)
  into requested_count, refund_total
  from jsonb_array_elements(payload->'lines') line;
  if refund_total <= 0 then raise exception 'Refund amount must be above zero'; end if;

  select count(*) into valid_count
  from jsonb_array_elements(payload->'lines') requested
  join public.pos_sales_order_lines sales_line
    on sales_line.id = nullif(requested->>'line_id', '')::bigint
    and sales_line.sales_order_id = selected_order.id
  left join lateral (
    select coalesce(sum(existing_refund.amount), 0) as refunded_amount
    from public.pos_sales_refund_lines existing_refund
    where existing_refund.sales_order_line_id = sales_line.id
  ) refunded on true
  where coalesce(nullif(requested->>'amount', '')::numeric, 0) > 0
    and coalesce(nullif(requested->>'amount', '')::numeric, 0) <= sales_line.line_total - refunded.refunded_amount;

  if valid_count <> requested_count then
    raise exception 'Refund line is invalid or exceeds the refundable amount';
  end if;

  insert into public.pos_sales_refunds (
    refund_code, sales_order_id, store_id, staff_name, method, reason, amount, refund_payload
  ) values (
    'RFD-' || floor(extract(epoch from clock_timestamp()) * 1000)::bigint,
    selected_order.id,
    selected_order.store_id,
    selected_staff.display_name,
    method_value,
    reason_value,
    refund_total,
    payload
  ) returning * into refund_row;

  insert into public.pos_sales_refund_lines (refund_id, sales_order_line_id, amount)
  select
    refund_row.id,
    sales_line.id,
    round(nullif(requested->>'amount', '')::numeric, 2)
  from jsonb_array_elements(payload->'lines') requested
  join public.pos_sales_order_lines sales_line
    on sales_line.id = nullif(requested->>'line_id', '')::bigint
    and sales_line.sales_order_id = selected_order.id;

  return jsonb_build_object(
    'ok', true,
    'refund_id', refund_row.refund_code,
    'order', public.pos_sales_order_payload(selected_order)
  );
end;
$$;

revoke execute on function public.pos_sales_order_payload(public.pos_sales_orders) from public, anon, authenticated;
revoke execute on function public.pos_repair_ticket_payload(public.pos_repair_tickets) from public, anon, authenticated;
revoke execute on function public.search_pos_repair_tickets(text, text, text, integer) from public;
revoke execute on function public.search_pos_sales_orders(text, text, text, integer, integer) from public;
revoke execute on function public.refund_pos_sales_order(text, jsonb) from public;

grant execute on function public.search_pos_repair_tickets(text, text, text, integer) to anon, authenticated, service_role;
grant execute on function public.search_pos_sales_orders(text, text, text, integer, integer) to anon, authenticated, service_role;
grant execute on function public.refund_pos_sales_order(text, jsonb) to anon, authenticated, service_role;
