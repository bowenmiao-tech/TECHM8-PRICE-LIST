      shift_code, store_id, business_date, opened_by, current_staff_name, last_staff_name, opened_at, last_seen_at
    ) values (
      shift_code_value, selected_store.id,
      coalesce(nullif(payload->>'business_date', '')::date, (now() at time zone 'Australia/Brisbane')::date),
