create or replace function public.save_pos_held_cart(session_token text, payload jsonb)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  selected_store public.store_locations%rowtype;
  selected_staff public.staff_directory%rowtype;
  hold_row public.pos_held_carts%rowtype;
  hold_code_value text;
begin
  if not public.is_valid_staff_session(session_token) then raise exception 'Invalid session'; end if;
  if jsonb_typeof(payload) <> 'object' then raise exception 'Held cart payload must be an object'; end if;
  if jsonb_typeof(payload->'items') <> 'array' or jsonb_array_length(payload->'items') = 0 then
    raise exception 'Held cart must contain at least one item';
  end if;

  select * into selected_store
  from public.store_locations store_location
  where store_location.active = true
    and store_location.store_code = coalesce(trim(payload->>'store_code'), '')
    and store_location.store_code <> 'warehouse';
  if not found then raise exception 'Store not found'; end if;

  select * into selected_staff
  from public.staff_directory staff
  where staff.active = true and lower(staff.display_name) = lower(coalesce(trim(payload->>'staff_name'), ''))
  limit 1;
  if not found then raise exception 'Staff member not found'; end if;

  hold_code_value := coalesce(nullif(trim(payload->>'id'), ''), 'HOLD-' || floor(extract(epoch from clock_timestamp()) * 1000)::bigint);
  insert into public.pos_held_carts (
    hold_code, store_id, customer_name, cart_payload, held_by, status
  ) values (
    hold_code_value, selected_store.id,
    coalesce(nullif(trim(payload->>'customer'), ''), 'Walk-in Customer'),
    payload, selected_staff.display_name, 'active'
  )
  on conflict (hold_code) do update set
    customer_name = excluded.customer_name,
    cart_payload = excluded.cart_payload,
    held_by = excluded.held_by,
    status = 'active',
    restored_by = null,
    restored_at = null
  where public.pos_held_carts.store_id = excluded.store_id
  returning * into hold_row;

  if not found then raise exception 'Held cart belongs to another store'; end if;
  return jsonb_build_object('ok', true, 'hold', public.pos_held_cart_payload(hold_row));
end;
$$;

