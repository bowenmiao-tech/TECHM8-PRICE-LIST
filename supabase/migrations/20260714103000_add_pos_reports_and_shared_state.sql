create or replace function public.get_pos_shift_payment_totals(session_token text, target_shift_code text)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  shift_row public.pos_store_shifts%rowtype;
  totals_payload jsonb;
begin
  if not public.is_valid_staff_session(session_token) then raise exception 'Invalid session'; end if;

  select * into shift_row
  from public.pos_store_shifts shift_record
  where shift_record.shift_code = coalesce(trim(target_shift_code), '')
  limit 1;
  if not found then raise exception 'Shift not found'; end if;

  with received as (
    select payment.method, round(sum(payment.amount), 2) as amount
    from public.pos_sales_order_payments payment
    join public.pos_sales_orders sales_order on sales_order.id = payment.sales_order_id
    where sales_order.store_id = shift_row.store_id
      and sales_order.shift_id = shift_row.shift_code
    group by payment.method
  ),
  refunded as (
    select refund.method, round(sum(refund.amount), 2) as amount
    from public.pos_sales_refunds refund
    where refund.store_id = shift_row.store_id
      and refund.created_at >= shift_row.opened_at
      and (shift_row.closed_at is null or refund.created_at <= shift_row.closed_at)
    group by refund.method
  ),
  methods as (
    select method from received
    union
    select method from refunded
  )
  select coalesce(jsonb_agg(jsonb_build_object(
    'method', methods.method,
    'received', coalesce(received.amount, 0),
    'refunded', coalesce(refunded.amount, 0),
    'amount', coalesce(received.amount, 0) - coalesce(refunded.amount, 0)
  ) order by methods.method), '[]'::jsonb)
  into totals_payload
  from methods
  left join received on received.method = methods.method
  left join refunded on refunded.method = methods.method;

  return jsonb_build_object(
    'ok', true,
    'shift_id', shift_row.shift_code,
    'payment_totals', totals_payload,
    'order_count', (
      select count(*) from public.pos_sales_orders sales_order
      where sales_order.store_id = shift_row.store_id and sales_order.shift_id = shift_row.shift_code
    ),
    'gross_sales', (
      select coalesce(sum(sales_order.total), 0) from public.pos_sales_orders sales_order
      where sales_order.store_id = shift_row.store_id and sales_order.shift_id = shift_row.shift_code
    )
  );
end;
$$;

create or replace function public.get_pos_sales_report(
  session_token text,
  target_store_code text,
  date_from date,
  date_to date,
  target_staff_name text default null
)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  selected_store public.store_locations%rowtype;
  from_value date := coalesce(date_from, (now() at time zone 'Australia/Brisbane')::date);
  to_value date := coalesce(date_to, date_from, (now() at time zone 'Australia/Brisbane')::date);
  staff_value text := nullif(trim(coalesce(target_staff_name, '')), '');
  report_payload jsonb;
begin
  if not public.is_valid_staff_session(session_token) then raise exception 'Invalid session'; end if;
  if from_value > to_value then raise exception 'Start date must not be after end date'; end if;
  if to_value - from_value > 366 then raise exception 'Report range cannot exceed 367 days'; end if;

  select * into selected_store
  from public.store_locations store_location
  where store_location.active = true
    and store_location.store_code = coalesce(trim(target_store_code), '')
    and store_location.store_code <> 'warehouse';
  if not found then raise exception 'Store not found'; end if;

  with sale_orders as (
    select sales_order.*
    from public.pos_sales_orders sales_order
    where sales_order.store_id = selected_store.id
      and sales_order.business_date between from_value and to_value
      and (staff_value is null or lower(sales_order.staff_name) = lower(staff_value))
  ),
  refund_rows as (
    select refund.*, (refund.created_at at time zone 'Australia/Brisbane')::date as business_date
    from public.pos_sales_refunds refund
    where refund.store_id = selected_store.id
      and (refund.created_at at time zone 'Australia/Brisbane')::date between from_value and to_value
      and (staff_value is null or lower(refund.staff_name) = lower(staff_value))
  ),
  sale_lines as (
    select sales_line.*
    from public.pos_sales_order_lines sales_line
    join sale_orders sales_order on sales_order.id = sales_line.sales_order_id
  ),
  refunded_lines as (
    select refund_line.amount, sales_line.line_type, sales_line.category, sales_line.quantity
    from public.pos_sales_refund_lines refund_line
    join refund_rows refund on refund.id = refund_line.refund_id
    join public.pos_sales_order_lines sales_line on sales_line.id = refund_line.sales_order_line_id
  ),
  summary as (
    select
      coalesce((select sum(total) from sale_orders), 0)::numeric(12,2) as gross_sales,
      coalesce((select sum(amount) from refund_rows), 0)::numeric(12,2) as refunds,
      coalesce((select count(*) from sale_orders), 0)::integer as invoice_count,
      coalesce((select count(*) from refund_rows), 0)::integer as refund_count,
      coalesce((select sum(quantity) from sale_lines), 0)::integer as item_units
  )
  select jsonb_build_object(
    'ok', true,
    'store_code', selected_store.store_code,
    'store_name', selected_store.store_name,
    'date_from', from_value,
    'date_to', to_value,
    'staff_name', staff_value,
    'summary', jsonb_build_object(
      'gross_sales', summary.gross_sales,
      'refunds', summary.refunds,
      'net_sales', summary.gross_sales - summary.refunds,
      'gst', round((summary.gross_sales - summary.refunds) / 11, 2),
      'invoice_count', summary.invoice_count,
      'refund_count', summary.refund_count,
      'item_units', summary.item_units,
      'average_sale', case when summary.invoice_count > 0 then round(summary.gross_sales / summary.invoice_count, 2) else 0 end
    ),
    'payment_totals', (
      select coalesce(jsonb_agg(jsonb_build_object('method', payment.method, 'amount', payment.amount) order by payment.amount desc), '[]'::jsonb)
      from (
        select order_payment.method, round(sum(order_payment.amount), 2) as amount
        from public.pos_sales_order_payments order_payment
        join sale_orders sales_order on sales_order.id = order_payment.sales_order_id
        group by order_payment.method
      ) payment
    ),
    'refund_method_totals', (
      select coalesce(jsonb_agg(jsonb_build_object('method', refund.method, 'amount', refund.amount) order by refund.amount desc), '[]'::jsonb)
      from (
        select refund_row.method, round(sum(refund_row.amount), 2) as amount
        from refund_rows refund_row
        group by refund_row.method
      ) refund
    ),
    'sale_type_totals', (
      select coalesce(jsonb_agg(jsonb_build_object(
        'type', line_type.line_type,
        'gross', line_type.gross,
        'refunds', line_type.refunds,
        'net', line_type.gross - line_type.refunds,
        'units', line_type.units
      ) order by line_type.gross - line_type.refunds desc), '[]'::jsonb)
      from (
        select type_key.line_type,
          coalesce((select sum(line_total) from sale_lines where sale_lines.line_type = type_key.line_type), 0)::numeric(12,2) as gross,
          coalesce((select sum(amount) from refunded_lines where refunded_lines.line_type = type_key.line_type), 0)::numeric(12,2) as refunds,
          coalesce((select sum(quantity) from sale_lines where sale_lines.line_type = type_key.line_type), 0)::integer as units
        from (
          select line_type from sale_lines
          union
          select line_type from refunded_lines
        ) type_key
      ) line_type
    ),
    'category_totals', (
      select coalesce(jsonb_agg(jsonb_build_object(
        'category', category.category,
        'gross', category.gross,
        'refunds', category.refunds,
        'net', category.gross - category.refunds,
        'units', category.units
      ) order by category.gross - category.refunds desc), '[]'::jsonb)
      from (
        select category_key.category,
          coalesce((select sum(line_total) from sale_lines where coalesce(nullif(trim(sale_lines.category), ''), 'Uncategorized') = category_key.category), 0)::numeric(12,2) as gross,
          coalesce((select sum(amount) from refunded_lines where coalesce(nullif(trim(refunded_lines.category), ''), 'Uncategorized') = category_key.category), 0)::numeric(12,2) as refunds,
          coalesce((select sum(quantity) from sale_lines where coalesce(nullif(trim(sale_lines.category), ''), 'Uncategorized') = category_key.category), 0)::integer as units
        from (
          select coalesce(nullif(trim(category), ''), 'Uncategorized') as category from sale_lines
          union
          select coalesce(nullif(trim(category), ''), 'Uncategorized') as category from refunded_lines
        ) category_key
      ) category
    ),
    'staff_totals', (
      select coalesce(jsonb_agg(jsonb_build_object(
        'staff_name', staff.staff_name,
        'gross', staff.gross,
        'refunds', staff.refunds,
        'net', staff.gross - staff.refunds,
        'invoice_count', staff.invoice_count,
        'refund_count', staff.refund_count
      ) order by staff.gross - staff.refunds desc), '[]'::jsonb)
      from (
        select staff_key.staff_name,
          coalesce((select sum(total) from sale_orders where sale_orders.staff_name = staff_key.staff_name), 0)::numeric(12,2) as gross,
          coalesce((select sum(amount) from refund_rows where refund_rows.staff_name = staff_key.staff_name), 0)::numeric(12,2) as refunds,
          coalesce((select count(*) from sale_orders where sale_orders.staff_name = staff_key.staff_name), 0)::integer as invoice_count,
          coalesce((select count(*) from refund_rows where refund_rows.staff_name = staff_key.staff_name), 0)::integer as refund_count
        from (
          select staff_name from sale_orders
          union
          select staff_name from refund_rows
        ) staff_key
      ) staff
    ),
    'daily_totals', (
      select coalesce(jsonb_agg(jsonb_build_object(
        'date', daily.business_date,
        'gross', daily.gross,
        'refunds', daily.refunds,
        'net', daily.gross - daily.refunds,
        'invoice_count', daily.invoice_count
      ) order by daily.business_date), '[]'::jsonb)
      from (
        select date_key.business_date,
          coalesce((select sum(total) from sale_orders where sale_orders.business_date = date_key.business_date), 0)::numeric(12,2) as gross,
          coalesce((select sum(amount) from refund_rows where refund_rows.business_date = date_key.business_date), 0)::numeric(12,2) as refunds,
          coalesce((select count(*) from sale_orders where sale_orders.business_date = date_key.business_date), 0)::integer as invoice_count
        from (
          select business_date from sale_orders
          union
          select business_date from refund_rows
        ) date_key
      ) daily
    )
  ) into report_payload
  from summary;

  return report_payload;
end;
$$;

