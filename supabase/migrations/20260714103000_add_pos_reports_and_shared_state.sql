create or replace function public.search_pos_customers(
  session_token text,
  target_store_code text,
  search_query text default '',
  result_limit integer default 500
)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  selected_store public.store_locations%rowtype;
  customers_payload jsonb;
  query_value text := trim(coalesce(search_query, ''));
  phone_query text := regexp_replace(coalesce(search_query, ''), '[^0-9]', '', 'g');
  safe_limit integer := least(greatest(coalesce(result_limit, 500), 1), 500);
begin
  if not public.is_valid_staff_session(session_token) then raise exception 'Invalid session'; end if;

  select * into selected_store
  from public.store_locations store_location
  where store_location.active = true
    and store_location.store_code = coalesce(trim(target_store_code), '')
    and store_location.store_code <> 'warehouse';
  if not found then raise exception 'Store not found'; end if;

  select coalesce(jsonb_agg(public.pos_customer_payload(customer) order by customer.updated_at desc), '[]'::jsonb)
  into customers_payload
  from (
    select customer.*
    from public.pos_customers customer
    where customer.store_id = selected_store.id
      and customer.active = true
      and (
        query_value = ''
        or customer.first_name ilike '%' || query_value || '%'
        or customer.last_name ilike '%' || query_value || '%'
        or concat_ws(' ', customer.first_name, customer.last_name) ilike '%' || query_value || '%'
        or customer.company ilike '%' || query_value || '%'
        or customer.phone ilike '%' || query_value || '%'
        or customer.alert_number ilike '%' || query_value || '%'
        or customer.email ilike '%' || query_value || '%'
        or (phone_query <> '' and customer.normalized_phone like '%' || phone_query || '%')
      )
    order by customer.updated_at desc
    limit safe_limit
  ) customer;

  return jsonb_build_object('ok', true, 'customers', customers_payload);
end;
$$;

create or replace function public.upsert_pos_customer(session_token text, payload jsonb)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  selected_store public.store_locations%rowtype;
  selected_staff public.staff_directory%rowtype;
  customer_row public.pos_customers%rowtype;
  customer_code_value text;
  first_name_value text := coalesce(trim(payload->>'first_name'), '');
  phone_value text := coalesce(trim(payload->>'phone'), '');
  alert_value text := coalesce(trim(payload->>'alert_number'), '');
begin
  if not public.is_valid_staff_session(session_token) then raise exception 'Invalid session'; end if;
  if jsonb_typeof(payload) <> 'object' then raise exception 'Customer payload must be an object'; end if;
  if first_name_value = '' then raise exception 'First name is required'; end if;
  if phone_value = '' and alert_value = '' then raise exception 'At least one phone number is required'; end if;

  select * into selected_store
  from public.store_locations store_location
  where store_location.active = true
    and store_location.store_code = coalesce(trim(payload->>'store_code'), '')
    and store_location.store_code <> 'warehouse';
  if not found then raise exception 'Store not found'; end if;

  select * into selected_staff
  from public.staff_directory staff
  where staff.active = true and lower(staff.display_name) = lower(coalesce(trim(payload->>'staff_name'), ''))
  limit 1;
  if not found then raise exception 'Staff member not found'; end if;

  customer_code_value := coalesce(nullif(trim(payload->>'id'), ''), 'CUS-' || floor(extract(epoch from clock_timestamp()) * 1000)::bigint);

  select * into customer_row
  from public.pos_customers customer
  where customer.customer_code = customer_code_value
  for update;

  if found then
    if customer_row.store_id <> selected_store.id then raise exception 'Customer belongs to another store'; end if;
    update public.pos_customers
    set
      first_name = first_name_value,
      last_name = coalesce(trim(payload->>'last_name'), ''),
      company = coalesce(trim(payload->>'company'), ''),
      phone = phone_value,
      normalized_phone = regexp_replace(phone_value, '[^0-9]', '', 'g'),
      alert_number = alert_value,
      email = lower(coalesce(trim(payload->>'email'), '')),
      customer_group = coalesce(nullif(trim(payload->>'group'), ''), 'Regular Customer'),
      notes = coalesce(trim(payload->>'notes'), ''),
      updated_by = selected_staff.display_name,
      active = true
    where customer_code = customer_code_value
    returning * into customer_row;
  else
    insert into public.pos_customers (
      customer_code, store_id, first_name, last_name, company, phone, normalized_phone,
      alert_number, email, customer_group, notes, created_by, updated_by
    ) values (
      customer_code_value, selected_store.id, first_name_value,
      coalesce(trim(payload->>'last_name'), ''), coalesce(trim(payload->>'company'), ''),
      phone_value, regexp_replace(phone_value, '[^0-9]', '', 'g'), alert_value,
      lower(coalesce(trim(payload->>'email'), '')),
      coalesce(nullif(trim(payload->>'group'), ''), 'Regular Customer'),
      coalesce(trim(payload->>'notes'), ''), selected_staff.display_name, selected_staff.display_name
    ) returning * into customer_row;
  end if;

  return jsonb_build_object('ok', true, 'customer', public.pos_customer_payload(customer_row));
end;
$$;

