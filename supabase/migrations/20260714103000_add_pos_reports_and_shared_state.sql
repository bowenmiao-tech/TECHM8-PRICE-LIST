create table if not exists public.pos_customers (
  id bigint generated by default as identity primary key,
  customer_code text not null unique,
  store_id bigint not null references public.store_locations(id) on delete restrict,
  first_name text not null,
  last_name text not null default '',
  company text not null default '',
  phone text not null default '',
  normalized_phone text not null default '',
  alert_number text not null default '',
  email text not null default '',
  customer_group text not null default 'Regular Customer',
  notes text not null default '',
  created_by text not null,
  updated_by text not null,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists pos_customers_store_name_idx
on public.pos_customers (store_id, lower(first_name), lower(last_name))
where active = true;

create index if not exists pos_customers_store_phone_idx
on public.pos_customers (store_id, normalized_phone)
where active = true and normalized_phone <> '';

drop trigger if exists pos_customers_set_updated_at on public.pos_customers;
create trigger pos_customers_set_updated_at
before update on public.pos_customers
for each row execute function public.set_updated_at();

create table if not exists public.pos_held_carts (
  id bigint generated by default as identity primary key,
  hold_code text not null unique,
  store_id bigint not null references public.store_locations(id) on delete restrict,
  customer_name text not null default 'Walk-in Customer',
  cart_payload jsonb not null,
  held_by text not null,
  status text not null default 'active' check (status in ('active', 'restored', 'discarded')),
  restored_by text,
  restored_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists pos_held_carts_store_active_idx
on public.pos_held_carts (store_id, created_at desc)
where status = 'active';

drop trigger if exists pos_held_carts_set_updated_at on public.pos_held_carts;
create trigger pos_held_carts_set_updated_at
before update on public.pos_held_carts
for each row execute function public.set_updated_at();

create table if not exists public.pos_store_shifts (
  id bigint generated by default as identity primary key,
  shift_code text not null unique,
  store_id bigint not null references public.store_locations(id) on delete restrict,
  business_date date not null,
  status text not null default 'open' check (status in ('open', 'closed')),
  opened_by text not null,
  current_staff_name text not null,
  last_staff_name text not null,
  opening_cash_total numeric(12,2) not null default 0 check (opening_cash_total >= 0),
  opening_counts jsonb not null default '{}'::jsonb,
  opening_confirmed_at timestamptz,
  system_totals jsonb not null default '{}'::jsonb,
  actual_totals jsonb not null default '{}'::jsonb,
  differences jsonb not null default '{}'::jsonb,
  closing_cash_total numeric(12,2),
  closing_counts jsonb not null default '{}'::jsonb,
  closed_by text,
  opened_at timestamptz not null default now(),
  last_seen_at timestamptz not null default now(),
  closed_at timestamptz,
  updated_at timestamptz not null default now()
);

create unique index if not exists pos_store_shifts_one_open_per_store_idx
on public.pos_store_shifts (store_id)
where status = 'open';

create index if not exists pos_store_shifts_store_date_idx
on public.pos_store_shifts (store_id, business_date desc, opened_at desc);

drop trigger if exists pos_store_shifts_set_updated_at on public.pos_store_shifts;
create trigger pos_store_shifts_set_updated_at
before update on public.pos_store_shifts
for each row execute function public.set_updated_at();

alter table public.pos_customers enable row level security;
