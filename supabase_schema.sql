language plpgsql
security definer
set search_path = public
as $$
declare
  reports_payload jsonb;
begin
  if not public.is_valid_admin_session(session_token) then
    raise exception 'Invalid admin session';
  end if;

  select coalesce(
    jsonb_agg(
      jsonb_build_object(
        'id', report.id,
        'report_code', report.report_code,
        'report_date', report.report_date,
        'store_name', report.store_name,
        'staff_name', report.staff_name,
        'notes', report.notes,
        'total_amount', report.total_amount,
        'repair_count', report.repair_count,
        'lcd_adjustment_count', report.lcd_adjustment_count,
        'submitted_at', report.submitted_at,
        'faulty_broken_lines', report.faulty_broken_lines_json,
        'device_sales_lines', report.device_sales_lines_json,
        'device_buyback_lines', report.device_buyback_lines_json,
        'end_of_day', report.end_of_day_json,
        'cash_expense_lines', report.cash_expense_lines_json,
        'customer_tracking', report.customer_tracking_json,
        'missing_sales_lines', report.missing_sales_lines_json,
        'repair_lines', coalesce((
          select jsonb_agg(
            jsonb_build_object(
              'line_order', repair.line_order,
              'model_and_color', repair.model_and_color,
              'problem_and_parts_used', repair.problem_and_parts_used,
              'tech_name', repair.tech_name,
              'invoice_number', repair.invoice_number,
              'amount', repair.amount
            )
            order by repair.line_order
          )
          from public.daily_report_repairs repair
          where repair.report_id = report.id
        ), '[]'::jsonb),
        'lcd_lines', coalesce((
          select jsonb_agg(
            jsonb_build_object(
              'line_order', lcd.line_order,
              'model_name', lcd.model_name,
              'variant_name', lcd.variant_name,
              'action_type', lcd.action_type,
              'quantity', lcd.quantity,
              'stock_before', lcd.stock_before,
              'stock_after', lcd.stock_after
            )
            order by lcd.line_order
          )
          from public.daily_report_lcd_lines lcd
          where lcd.report_id = report.id
        ), '[]'::jsonb)
      )
      order by report.report_date desc, report.submitted_at desc, report.id desc
    ),
    '[]'::jsonb
  )
  into reports_payload
  from public.daily_report_submissions report
  join public.store_locations stores on stores.id = report.store_id
  where (
      coalesce(target_date, '') = ''
      or report.report_date = target_date::date
    )
    and (
      coalesce(target_store_code, '') = ''
      or stores.store_code = target_store_code
    );

  return jsonb_build_object('ok', true, 'reports', reports_payload);
end;
