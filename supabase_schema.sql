create or replace function public.confirm_inventory_transfer_received(
  session_token text,
  target_transfer_id bigint,
  target_store_code text,
  staff_name text,
  target_receipt_photo_url text
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  selected_store public.store_locations%rowtype;
  selected_staff public.staff_directory%rowtype;
  transfer_row public.inventory_transfers%rowtype;
  line_row record;
  upserted_item_id bigint;
  updated_line_count integer := 0;
begin
  if not public.is_valid_staff_session(session_token) then
    raise exception 'Invalid session';
  end if;

  perform public.cleanup_old_inventory_transfers();

  if coalesce(trim(target_receipt_photo_url), '') = '' then
    raise exception 'Receipt photo is required';
  end if;

  select *
  into selected_store
  from public.store_locations
  where active = true
    and store_code = coalesce(trim(target_store_code), '')
    and store_code <> 'warehouse';

  if not found then
    raise exception 'Store not found';
  end if;

  select *
  into selected_staff
  from public.staff_directory
  where active = true
    and display_name = coalesce(trim(staff_name), '');

  if not found then
    raise exception 'Staff member not found';
  end if;

  select *
  into transfer_row
  from public.inventory_transfers
  where id = target_transfer_id
    and to_store_id = selected_store.id
  for update;

  if not found then
    raise exception 'Transfer not found for this store';
  end if;

  if transfer_row.status <> 'pending' then
    raise exception 'This transfer is not pending';
  end if;

  for line_row in
    select *
    from public.inventory_transfer_lines
    where transfer_id = transfer_row.id
    order by line_order
  loop
    insert into public.lcd_inventory_items (
      store_id,
      model_name,
      variant_name,
      category_key,
      current_qty,
      active
    )
    values (
      selected_store.id,
      line_row.model_name,
      line_row.variant_name,
      line_row.category_key,
      line_row.quantity,
      true
    )
    on conflict (store_id, model_name, variant_name) do update
    set
      category_key = excluded.category_key,
      current_qty = public.lcd_inventory_items.current_qty + excluded.current_qty,
      active = true,
      updated_at = now()
    returning id into upserted_item_id;

    update public.inventory_transfer_lines
    set target_inventory_item_id = upserted_item_id
    where id = line_row.id;

    updated_line_count := updated_line_count + 1;
  end loop;

  update public.inventory_transfers
  set
    status = 'received',
    received_by = selected_staff.display_name,
    receipt_photo_url = trim(target_receipt_photo_url),
    received_at = now(),
    updated_at = now()
  where id = transfer_row.id;

  return jsonb_build_object(
    'ok', true,
    'transfer_id', transfer_row.id,
    'updated_rows', updated_line_count
  );
end;
$$;

