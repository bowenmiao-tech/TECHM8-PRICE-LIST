create or replace function public.review_lcd_inventory_count(
  session_token text,
  target_submission_id bigint,
  decision text,
  review_note text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  submission_row public.lcd_inventory_count_submissions%rowtype;
  line_row record;
  normalized_decision text;
  updated_rows integer := 0;
begin
  if not public.is_valid_admin_session(session_token) then
    raise exception 'Invalid admin session';
  end if;

  normalized_decision := lower(coalesce(trim(decision), ''));
  if normalized_decision not in ('approved', 'rejected') then
    raise exception 'Decision must be approved or rejected';
  end if;

  select *
  into submission_row
  from public.lcd_inventory_count_submissions
  where id = target_submission_id
  for update;

  if not found then
    raise exception 'Inventory count submission not found';
  end if;

  if submission_row.status <> 'pending' then
    raise exception 'This inventory count has already been reviewed';
  end if;

  if normalized_decision = 'approved' then
    for line_row in
      select *
      from public.lcd_inventory_count_lines
      where submission_id = submission_row.id
      order by line_order
    loop
      if line_row.lcd_inventory_item_id is null then
        continue;
      end if;

      update public.lcd_inventory_items
      set current_qty = line_row.counted_qty
      where id = line_row.lcd_inventory_item_id;

      updated_rows := updated_rows + 1;
    end loop;
  end if;

  update public.lcd_inventory_count_submissions
  set
    status = normalized_decision,
    review_note = nullif(trim(coalesce(review_lcd_inventory_count.review_note, '')), ''),
    reviewed_at = now(),
    updated_at = now()
  where id = submission_row.id;

  return jsonb_build_object(
    'ok', true,
    'status', normalized_decision,
    'updated_rows', updated_rows
  );
end;
$$;

