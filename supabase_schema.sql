    raise exception 'Model and variant are required';
  end if;

  if row_current_qty < 0 then
    raise exception 'Quantity cannot be negative';
  end if;

  if row_category_key not in ('iphone', 'samsung', 'pixel', 'ipad', 'oppo', 'other') then
    raise exception 'Invalid inventory category';
  end if;

  insert into public.lcd_inventory_items (
    store_id,
    model_name,
    variant_name,
    category_key,
    current_qty,
    active
  )
  values (
    target_store.id,
    row_model_name,
    row_variant_name,
    row_category_key,
    row_current_qty,
    row_active
  )
  on conflict (store_id, model_name, variant_name) do update
  set
    category_key = excluded.category_key,
    current_qty = excluded.current_qty,
    active = excluded.active,
    updated_at = now()
  returning jsonb_build_object(
    'id', public.lcd_inventory_items.id,
    'store_id', target_store.id,
    'store_code', target_store.store_code,
    'store_name', target_store.store_name,
    'model_name', public.lcd_inventory_items.model_name,
    'variant_name', public.lcd_inventory_items.variant_name,
    'category_key', public.lcd_inventory_items.category_key,
    'current_qty', public.lcd_inventory_items.current_qty,
    'active', public.lcd_inventory_items.active,
    'updated_at', public.lcd_inventory_items.updated_at
  )
  into saved_item;

  return jsonb_build_object('ok', true, 'item', saved_item);
end;
$$;

