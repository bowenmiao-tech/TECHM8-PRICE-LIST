declare
  merge_row record;
begin
  for merge_row in
    select *
    from (
      values
        (
          'iphone',
          'iPhone 8 / SE2 / SE3',
          array['iPhone 8', 'iPhone SE2', 'iPhone SE3']::text[],
          array['Black', 'White']::text[]
        ),
        (
          'ipad',
          'iPad Air 1 / 9.7 2017 / 6th 9.7 2018 / 10.2 2019-2021',
          array['iPad Air1 / iPad 5th', 'iPad Air 1', 'iPad 5th', 'iPad 9.7 (2017)', 'iPad 6th Gen', 'iPad 6th 9.7 (2018)', 'iPad 7,8,9 Gen(10.2)', 'iPad 10.2 (2019)', 'iPad 10.2 (2020)', 'iPad 10.2 (2021)']::text[],
          array['Battery']::text[]
        ),
        (
          'ipad',
          'iPad Air 5 / iPad 10 2022 / iPad 11 / iPad Air 11 2024',
          array['iPad Air 5', 'iPad 10th', 'iPad 10 (2022)', 'iPad 11', 'iPad Air 11 (2024)', 'Air11 (2024)']::text[],
          array['Battery']::text[]
        )
    ) as compatibility_groups(category_key, target_model_name, source_model_names, variant_names)
  loop
    insert into public.lcd_inventory_items (store_id, model_name, variant_name, category_key, current_qty, active)
    select
      stores.id,
      merge_row.target_model_name,
      variant_name,
      merge_row.category_key,
      0,
      true
    from public.store_locations stores
    cross join unnest(merge_row.variant_names) as variants(variant_name)
    where stores.active = true
    on conflict (store_id, model_name, variant_name) do update
    set
      category_key = excluded.category_key,
      active = true,
      updated_at = now();

    with source_totals as (
      select
        source_items.store_id,
        source_items.variant_name,
        sum(source_items.current_qty)::integer as source_qty
      from public.lcd_inventory_items source_items
      where source_items.active = true
        and source_items.model_name = any(merge_row.source_model_names)
        and source_items.variant_name = any(merge_row.variant_names)
        and source_items.model_name <> merge_row.target_model_name
      group by source_items.store_id, source_items.variant_name
    )
    update public.lcd_inventory_items target_items
    set
      current_qty = target_items.current_qty + source_totals.source_qty,
      category_key = merge_row.category_key,
      active = true,
      updated_at = now()
    from source_totals
    where target_items.store_id = source_totals.store_id
      and target_items.model_name = merge_row.target_model_name
      and target_items.variant_name = source_totals.variant_name;

    update public.lcd_inventory_items source_items
    set
      current_qty = 0,
      active = false,
      updated_at = now()
    where source_items.active = true
      and source_items.model_name = any(merge_row.source_model_names)
      and source_items.variant_name = any(merge_row.variant_names)
      and source_items.model_name <> merge_row.target_model_name;
  end loop;
end;
