create or replace function public.change_admin_account_password(session_token text, new_password text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  target_admin_id bigint;
begin
  if not public.is_valid_admin_session(session_token) then
    raise exception 'Invalid admin session';
  end if;

  if length(coalesce(new_password, '')) < 8 then
    raise exception 'Password must be at least 8 characters';
  end if;

  select admin_user_id
  into target_admin_id
  from public.admin_sessions
  where expires_at > now()
    and extensions.crypt(session_token, session_hash) = session_hash
  order by id desc
  limit 1;

  if target_admin_id is null then
    raise exception 'Admin session not found';
  end if;

  update public.admin_users
  set password_hash = extensions.crypt(new_password, extensions.gen_salt('bf'))
  where id = target_admin_id;

  delete from public.admin_sessions
  where admin_user_id = target_admin_id;

  return jsonb_build_object('ok', true);
end;
$$;

