create or replace function public.create_inventory_transfer(
  session_token text,
  payload jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  warehouse_store public.store_locations%rowtype;
  target_store public.store_locations%rowtype;
  transfer_id_value bigint;
  transfer_code_value text;
  lines_payload jsonb;
  line_payload jsonb;
  source_item public.lcd_inventory_items%rowtype;
  line_quantity integer;
  line_order_value integer := 0;
  created_by_value text;
  notes_value text;
  source_type_value text;
  line_model_name text;
  line_variant_name text;
  line_category_key text;
begin
  if not public.is_valid_admin_session(session_token) then
    raise exception 'Invalid admin session';
  end if;

  perform public.cleanup_old_inventory_transfers();

  if jsonb_typeof(payload) <> 'object' then
    raise exception 'Payload must be a JSON object';
  end if;

  source_type_value := lower(coalesce(nullif(trim(payload->>'source_type'), ''), 'warehouse'));
  if source_type_value not in ('warehouse', 'supplier') then
    raise exception 'Transfer source must be warehouse or supplier';
  end if;

  if source_type_value = 'warehouse' then
    select *
    into warehouse_store
    from public.store_locations
    where store_code = 'warehouse'
      and active = true;

    if not found then
      raise exception 'Warehouse store not found';
    end if;
  end if;

  select *
  into target_store
  from public.store_locations
  where store_code = coalesce(trim(payload->>'to_store_code'), '')
    and active = true
    and store_code <> 'warehouse';

  if not found then
    raise exception 'Target store not found';
  end if;

  lines_payload := coalesce(payload->'lines', '[]'::jsonb);
  if jsonb_typeof(lines_payload) <> 'array' or jsonb_array_length(lines_payload) = 0 then
    raise exception 'At least one transfer line is required';
  end if;

  created_by_value := coalesce(trim(payload->>'created_by'), '');
  notes_value := coalesce(trim(payload->>'notes'), '');

  insert into public.inventory_transfers (
    source_type,
    from_store_id,
    to_store_id,
    from_store_name,
    to_store_name,
    status,
    notes,
    created_by
  )
  values (
    source_type_value,
    case when source_type_value = 'warehouse' then warehouse_store.id else null end,
    target_store.id,
    case when source_type_value = 'warehouse' then warehouse_store.store_name else 'Supplier' end,
    target_store.store_name,
    'pending',
    notes_value,
    created_by_value
  )
  returning id into transfer_id_value;

  transfer_code_value := 'TR-' || to_char(current_date, 'YYYYMMDD') || '-' || lpad(transfer_id_value::text, 5, '0');

  for line_payload in
    select value
    from jsonb_array_elements(lines_payload)
  loop
    if jsonb_typeof(line_payload) <> 'object' then
      continue;
    end if;

    line_quantity := coalesce(nullif(trim(line_payload->>'quantity'), ''), '0')::integer;
    if line_quantity <= 0 then
      raise exception 'Transfer quantity must be greater than 0';
    end if;

    if source_type_value = 'warehouse' then
      select *
      into source_item
      from public.lcd_inventory_items
      where id = (line_payload->>'inventory_item_id')::bigint
        and store_id = warehouse_store.id
        and active = true
      for update;

      if not found then
        raise exception 'Warehouse inventory item not found';
      end if;

      if source_item.current_qty < line_quantity then
        raise exception 'Not enough warehouse stock for % / %', source_item.model_name, source_item.variant_name;
      end if;

      update public.lcd_inventory_items
      set current_qty = current_qty - line_quantity
      where id = source_item.id;

      line_model_name := source_item.model_name;
      line_variant_name := source_item.variant_name;
      line_category_key := source_item.category_key;
    else
      line_model_name := coalesce(trim(line_payload->>'model_name'), '');
      line_variant_name := coalesce(trim(line_payload->>'variant_name'), '');
      line_category_key := lower(coalesce(nullif(trim(line_payload->>'category_key'), ''), 'other'));

      if line_model_name = '' or line_variant_name = '' then
        raise exception 'Supplier transfer lines require model and part';
      end if;

      if line_category_key not in ('iphone', 'samsung', 'pixel', 'ipad', 'oppo', 'other') then
        raise exception 'Invalid supplier transfer category';
      end if;
    end if;

    line_order_value := line_order_value + 1;

    insert into public.inventory_transfer_lines (
      transfer_id,
      line_order,
      source_inventory_item_id,
      model_name,
      variant_name,
      category_key,
      quantity
    )
    values (
      transfer_id_value,
      line_order_value,
      case when source_type_value = 'warehouse' then source_item.id else null end,
      line_model_name,
      line_variant_name,
      line_category_key,
      line_quantity
    );
  end loop;

  if line_order_value = 0 then
    raise exception 'At least one valid transfer line is required';
  end if;

  update public.inventory_transfers
  set transfer_code = transfer_code_value
  where id = transfer_id_value;

  return jsonb_build_object(
    'ok', true,
    'transfer_id', transfer_id_value,
    'transfer_code', transfer_code_value,
    'line_count', line_order_value
  );
end;
$$;

create or replace function public.get_inventory_transfers(
  session_token text,
  target_status text default null,
  target_store_code text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  transfers_payload jsonb;
begin
  if not public.is_valid_admin_session(session_token) then
    raise exception 'Invalid admin session';
  end if;

  perform public.cleanup_old_inventory_transfers();

  select coalesce(
    jsonb_agg(
      jsonb_build_object(
        'id', transfer.id,
        'transfer_code', transfer.transfer_code,
        'source_type', transfer.source_type,
        'from_store_name', transfer.from_store_name,
        'to_store_code', target_store.store_code,
        'to_store_name', transfer.to_store_name,
        'status', transfer.status,
        'notes', transfer.notes,
        'created_by', transfer.created_by,
        'received_by', transfer.received_by,
        'receipt_photo_url', transfer.receipt_photo_url,
        'created_at', transfer.created_at,
        'received_at', transfer.received_at,
        'lines', coalesce((
          select jsonb_agg(
            jsonb_build_object(
              'id', line.id,
              'model_name', line.model_name,
              'variant_name', line.variant_name,
              'category_key', line.category_key,
              'quantity', line.quantity
            )
            order by line.line_order
          )
          from public.inventory_transfer_lines line
          where line.transfer_id = transfer.id
        ), '[]'::jsonb)
      )
      order by transfer.created_at desc, transfer.id desc
    ),
    '[]'::jsonb
  )
  into transfers_payload
  from public.inventory_transfers transfer
  join public.store_locations target_store on target_store.id = transfer.to_store_id
  where (
      coalesce(trim(target_status), '') = ''
      or transfer.status = trim(target_status)
    )
    and (
      coalesce(trim(target_store_code), '') = ''
      or target_store.store_code = trim(target_store_code)
    )
    and (
      transfer.status = 'pending'
      or coalesce(transfer.received_at, transfer.updated_at, transfer.created_at) >= now() - interval '7 days'
    );

  return jsonb_build_object('ok', true, 'transfers', transfers_payload);
end;
$$;

create or replace function public.cancel_inventory_transfer(
  session_token text,
  target_transfer_id bigint
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  transfer_row public.inventory_transfers%rowtype;
  line_row record;
  restored_rows integer := 0;
begin
  if not public.is_valid_admin_session(session_token) then
    raise exception 'Invalid admin session';
  end if;

  perform public.cleanup_old_inventory_transfers();

  select *
  into transfer_row
  from public.inventory_transfers
  where id = target_transfer_id
  for update;

  if not found then
    raise exception 'Transfer not found';
  end if;

  if transfer_row.status <> 'pending' then
    raise exception 'Only pending transfers can be cancelled';
  end if;

  if transfer_row.source_type = 'warehouse' then
    for line_row in
      select *
      from public.inventory_transfer_lines
      where transfer_id = transfer_row.id
      order by line_order
    loop
      if line_row.source_inventory_item_id is null then
        continue;
      end if;

      update public.lcd_inventory_items
      set
        current_qty = current_qty + line_row.quantity,
        active = true,
        updated_at = now()
      where id = line_row.source_inventory_item_id;

      restored_rows := restored_rows + 1;
    end loop;
  end if;

  update public.inventory_transfers
  set
    status = 'cancelled',
    updated_at = now()
  where id = transfer_row.id;

  return jsonb_build_object(
    'ok', true,
    'transfer_id', transfer_row.id,
    'status', 'cancelled',
    'restored_rows', restored_rows
  );
end;
$$;

