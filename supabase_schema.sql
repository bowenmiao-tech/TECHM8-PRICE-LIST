create or replace function public.create_staff_session(input_password text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  stored_hash text;
  issued_token text;
  session_expires_at timestamptz;
begin
  stored_hash := (
    select password_hash
    from public.admin_config
    where id = true
  );

  if stored_hash is null or extensions.crypt(input_password, stored_hash) <> stored_hash then
    return jsonb_build_object('ok', false, 'message', 'Incorrect password');
  end if;

  delete from public.staff_sessions
  where expires_at <= now();

  issued_token := encode(extensions.gen_random_bytes(24), 'hex');
  session_expires_at := now() + interval '12 hours';

  insert into public.staff_sessions (session_hash, expires_at)
  values (extensions.crypt(issued_token, extensions.gen_salt('bf')), session_expires_at);

  return jsonb_build_object(
    'ok', true,
    'session_token', issued_token,
    'expires_at', session_expires_at
  );
end;
$$;

