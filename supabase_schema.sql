create or replace function public.upsert_lcd_inventory_item(session_token text, row_payload jsonb)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  target_store public.store_locations%rowtype;
  row_model_name text;
  row_variant_name text;
  row_category_key text;
  row_current_qty integer;
  row_active boolean;
  saved_item jsonb;
begin
  if not public.is_valid_admin_session(session_token) then
    raise exception 'Invalid admin session';
  end if;

  if jsonb_typeof(row_payload) <> 'object' then
    raise exception 'Row payload must be a JSON object';
  end if;

  select *
  into target_store
  from public.store_locations
  where store_code = coalesce(trim(row_payload->>'store_code'), '');

  if not found then
    raise exception 'Store not found';
  end if;

  row_model_name := coalesce(trim(row_payload->>'model_name'), '');
  row_variant_name := coalesce(trim(row_payload->>'variant_name'), '');
  row_category_key := lower(coalesce(trim(row_payload->>'category_key'), 'other'));
  row_current_qty := coalesce(nullif(trim(row_payload->>'current_qty'), ''), '0')::integer;
  row_active := coalesce((row_payload->>'active')::boolean, true);

